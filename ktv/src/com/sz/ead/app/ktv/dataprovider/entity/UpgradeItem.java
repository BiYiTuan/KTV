package com.sz.ead.app.ktv.dataprovider.entity;

public class UpgradeItem 
{
	private String mVersion;    //版本号
	private String mMD5;        //文件MD5校验值
	private String mUrl;        //该版本软件包下载地址
	private String mMessage;    //升级提示信息（多语言）
	private int    mIsupgrade;  //是否需要升级: 0-不需要 1-需要
	private int    mIsforce;    //是否强制升级: 0-不强制 1-强制
	private int    mUpgradeway; //升级方式：0-全量，1-增量
	
	public void setVersion(String version)
	{
		mVersion = version;
	}
	
	public String getVersion()
	{
		return mVersion;
	}
	
	public void setMD5(String md5)
	{
		mMD5 = md5;
	}
	
	public String getMD5()
	{
		return mMD5;
	}
	
	public void setUrl(String url)
	{
		mUrl = url;
	}
	
	public String getUrl()
	{
		return mUrl;
	}
	
	public void setMessage(String message)
	{
		mMessage = message;
	}
	
	public String getMessage()
	{
		return mMessage;
	}
	
	public void setIsupgrade(int isupgrade)
	{
		mIsupgrade = isupgrade;
	}
	
	public int getIsupgrade()
	{
		return mIsupgrade;
	}
	
	public void setIsforce(int isforce)
	{
		mIsforce = isforce;
	}
	
	public int getIsforce()
	{
		return mIsforce;
	}
	
	public void setUpgradeway(int upgrade)
	{
		mUpgradeway = upgrade;
	}
	
	public int getUpgradeway()
	{
		return mUpgradeway;
	}
}
